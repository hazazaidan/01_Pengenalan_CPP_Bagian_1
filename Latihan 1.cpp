#include <iostream>
using namespace std;

int main() {
    float num1, num2;

    // Input dua bilangan float
    cout << "Masukkan dua bilangan float: ";
    cin >> num1 >> num2;

    // Operasi aritmatika
    cout << "Penjumlahan: " << num1 + num2 << endl;
    cout << "Pengurangan: " << num1 - num2 << endl;
    cout << "Perkalian: " << num1 * num2 << endl;
    cout << "Pembagian: " << num1 / num2 << endl;

    return 0;
}
